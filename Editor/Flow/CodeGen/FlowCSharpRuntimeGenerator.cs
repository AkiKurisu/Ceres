            public SourceContext(FlowGraph graph, string className)
            {
                _graph = graph;
                _className = className;
                _nodes = graph.nodes.ToDictionary(node => node.Guid);
                foreach (var node in graph.nodes.OfType<PropertyNode_SharedVariableValue>())
                {
