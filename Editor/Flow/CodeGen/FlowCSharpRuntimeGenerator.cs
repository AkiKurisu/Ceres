        private static string SanitizeIdentifier(string value)
        {
            if (string.IsNullOrEmpty(value)) return "Unnamed";
            var builder = new StringBuilder(value.Length);
            foreach (var c in value)
            {
                builder.Append(char.IsLetterOrDigit(c) ? c : '_');
            }

            if (builder.Length == 0 || char.IsDigit(builder[0]))
            {
                builder.Insert(0, '_');
            }

            return builder.ToString();
        }

        private static bool CanDirectCall(FlowNode_ExecuteFunction node, out DirectCallInfo directCall)
        {
            directCall = default;

            if (!TryGetMethodCallInfo(node, out var methodInfo, out var targetType, out var declaringType,
                    out var parameterTypes))
            {
                return false;
            }

            if (!methodInfo.IsPublic || !IsVisibleType(declaringType))
            {
                return false;
            }

            directCall = new DirectCallInfo(declaringType, targetType, methodInfo.Name, methodInfo.ReturnType,
                parameterTypes, false);
            return true;
        }

        private static bool CanGeneratedCall(FlowNode_ExecuteFunction node, out DirectCallInfo directCall)
        {
            if (CanDirectCall(node, out directCall))
            {
                return true;
            }

            if (!TryGetMethodCallInfo(node, out var methodInfo, out var targetType, out var declaringType,
                    out var parameterTypes))
            {
                return false;
            }

            directCall = new DirectCallInfo(declaringType, targetType, methodInfo.Name, methodInfo.ReturnType,
                parameterTypes, true);
            return true;
        }

        private static bool TryGetMethodCallInfo(FlowNode_ExecuteFunction node, out MethodInfo methodInfo,
            out Type targetType, out Type declaringType, out Type[] parameterTypes)
        {
            methodInfo = null;
            targetType = null;
            declaringType = null;
            parameterTypes = null;

            targetType = GetTargetType(node);
            if (targetType == null || !IsVisibleType(targetType))
            {
                return false;
            }

            try
            {
                methodInfo = node.GetMethodInfo(targetType);
            }
            catch
            {
                return false;
            }

            if (methodInfo == null ||
                methodInfo.IsStatic != node.isStatic ||
                methodInfo.ContainsGenericParameters ||
                methodInfo.IsGenericMethodDefinition ||
                !IsVisibleType(methodInfo.ReturnType))
            {
                return false;
            }

            declaringType = methodInfo.DeclaringType ?? targetType;

            var parameters = methodInfo.GetParameters();
            if (parameters.Length > 6)
            {
                return false;
            }

            parameterTypes = new Type[parameters.Length];
            for (var i = 0; i < parameters.Length; i++)
            {
                var parameterType = parameters[i].ParameterType;
                if (parameterType.IsByRef ||
                    parameterType.IsPointer ||
                    parameterType.IsGenericParameter ||
                    !IsVisibleType(parameterType))
                {
                    return false;
                }

                parameterTypes[i] = parameterType;
            }

            return true;
        }

        private static bool CanDirectGetProperty(PropertyNode_PropertyValue node, out PropertyCallInfo propertyCall)
        {
            return CanDirectProperty(node, true, out propertyCall);
        }

        private static bool CanDirectSetProperty(PropertyNode_PropertyValue node, out PropertyCallInfo propertyCall)
        {
            return CanDirectProperty(node, false, out propertyCall);
        }

        private static bool CanDirectProperty(PropertyNode_PropertyValue node, bool getter,
            out PropertyCallInfo propertyCall)
        {
            propertyCall = default;

            var targetType = GetPropertyTargetType(node);
            if (targetType == null || !IsVisibleType(targetType))
            {
                return false;
            }

            var propertyInfo = node.GetPropertyInfo(targetType);
            if (propertyInfo == null ||
                !IsVisibleType(propertyInfo.PropertyType))
            {
                return false;
            }

            var accessor = getter ? propertyInfo.GetGetMethod(false) : propertyInfo.GetSetMethod(false);
            if (accessor == null || !accessor.IsPublic || accessor.IsStatic != node.isStatic)
            {
                return false;
            }

            var declaringType = propertyInfo.DeclaringType ?? targetType;
            if (!IsVisibleType(declaringType))
            {
                return false;
            }

            propertyCall = new PropertyCallInfo(declaringType, targetType, propertyInfo.Name,
                propertyInfo.PropertyType);
            return true;
        }

        private static bool CanAccessSharedVariable(PropertyNode_SharedVariableValue node, out Type variableType,
            out Type valueType)
        {
            variableType = null;
            valueType = null;

            var arguments = GetGenericArguments(node);
            if (arguments.Length < 3)
            {
                return false;
            }

            variableType = arguments[0];
            valueType = arguments[2];
            return IsVisibleType(variableType) && IsVisibleType(valueType);
        }

        private static Type GetTargetType(FlowNode_ExecuteFunction node)
        {
            var arguments = GetGenericArguments(node);
            return arguments.Length > 0 ? arguments[0] : null;
        }

        private static Type GetPropertyTargetType(PropertyNode_PropertyValue node)
        {
            var arguments = GetGenericArguments(node);
            return arguments.Length > 0 ? arguments[0] : null;
        }

        private static bool TryGetSelfReferenceTargetType(PropertyNode node, out Type targetType)
        {
            targetType = null;
            if (!IsGenericInstance(node, typeof(PropertyNode_GetSelfTReference<>)))
            {
                return false;
            }

            var arguments = GetGenericArguments(node);
            if (arguments.Length == 0 ||
                !typeof(UObject).IsAssignableFrom(arguments[0]) ||
                !IsVisibleType(arguments[0]))
            {
                return false;
            }

            targetType = arguments[0];
            return true;
        }

        private static Type[] GetGenericArguments(object value)
        {
            var type = value.GetType();
            while (type != null)
            {
                if (type.IsGenericType)
                {
                    return type.GetGenericArguments();
                }

                type = type.BaseType;
            }

            return Type.EmptyTypes;
        }

        private static bool IsGenericInstance(object value, Type genericTypeDefinition)
        {
            var type = value.GetType();
            while (type != null)
            {
                if (type.IsGenericType && type.GetGenericTypeDefinition() == genericTypeDefinition)
                {
                    return true;
                }

                type = type.BaseType;
            }

            return false;
        }

        private static bool IsVisibleType(Type type)
        {
            if (type == null) return false;
            if (type.IsByRef || type.IsPointer || type.IsGenericParameter) return false;
            if (type.IsArray) return IsVisibleType(type.GetElementType());
            if (!IsVisibleTypeDefinition(type)) return false;

            return !type.IsGenericType || type.GetGenericArguments().All(IsVisibleType);
        }

        private static bool IsVisibleTypeDefinition(Type type)
        {
            if (!type.IsNested)
            {
                return type.IsPublic;
            }

            while (type != null && type.IsNested)
            {
                if (!type.IsNestedPublic)
                {
                    return false;
                }

                type = type.DeclaringType;
            }

            return type == null || type.IsPublic;
        }

