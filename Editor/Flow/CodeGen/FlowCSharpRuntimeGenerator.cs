        internal static string SanitizeIdentifier(string value)
        {
            if (string.IsNullOrEmpty(value)) return "Unnamed";
            var builder = new StringBuilder(value.Length);
            foreach (var c in value)
            {
                builder.Append(char.IsLetterOrDigit(c) ? c : '_');
            }

            if (builder.Length == 0 || char.IsDigit(builder[0]))
            {
                builder.Insert(0, '_');
            }

            return builder.ToString();
        }

        internal static bool CanDirectCall(FlowNode_ExecuteFunction node, out DirectCallInfo directCall)
        {
            directCall = default;

            if (!TryGetMethodCallInfo(node, out var methodInfo, out var targetType, out var declaringType,
                    out var parameterTypes))
            {
                return false;
            }

            if (!methodInfo.IsPublic || !IsVisibleType(declaringType))
            {
                return false;
            }

            directCall = new DirectCallInfo(declaringType, targetType, methodInfo, methodInfo.ReturnType,
                parameterTypes, false);
            return true;
        }

        internal static bool CanGeneratedCall(FlowNode_ExecuteFunction node, out DirectCallInfo directCall)
        {
            if (CanDirectCall(node, out directCall))
            {
                return true;
            }

            if (!TryGetMethodCallInfo(node, out var methodInfo, out var targetType, out var declaringType,
                    out var parameterTypes))
            {
                return false;
            }

            directCall = new DirectCallInfo(declaringType, targetType, methodInfo, methodInfo.ReturnType,
                parameterTypes, true);
            return true;
        }

        private static bool TryGetMethodCallInfo(FlowNode_ExecuteFunction node, out MethodInfo methodInfo,
            out Type targetType, out Type declaringType, out Type[] parameterTypes)
        {
            methodInfo = null;
            targetType = null;
            declaringType = null;
            parameterTypes = null;

            targetType = GetTargetType(node);
            if (targetType == null || !IsVisibleType(targetType))
            {
                return false;
            }

            try
            {
                methodInfo = node.GetMethodInfo(targetType);
            }
            catch
            {
                return false;
            }

            if (methodInfo == null ||
                methodInfo.IsStatic != node.isStatic ||
                methodInfo.ContainsGenericParameters ||
                methodInfo.IsGenericMethodDefinition ||
                !IsVisibleType(methodInfo.ReturnType))
            {
                return false;
            }

            declaringType = methodInfo.DeclaringType ?? targetType;

            var parameters = methodInfo.GetParameters();
            if (parameters.Length > 6)
            {
                return false;
            }

            parameterTypes = new Type[parameters.Length];
            for (var i = 0; i < parameters.Length; i++)
            {
                var parameterType = parameters[i].ParameterType;
                if (parameterType.IsByRef ||
                    parameterType.IsPointer ||
                    parameterType.IsGenericParameter ||
                    !IsVisibleType(parameterType))
                {
                    return false;
                }

                parameterTypes[i] = parameterType;
            }

            return true;
        }

