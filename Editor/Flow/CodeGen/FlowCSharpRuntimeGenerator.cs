        internal static bool CanAccessSharedVariable(PropertyNode_SharedVariableValue node, out Type variableType,
            out Type variableValueType, out Type portValueType)
        {
            variableType = null;
            variableValueType = null;
            portValueType = null;

            var arguments = GetGenericArguments(node);
            if (arguments.Length < 3)
            {
                return false;
            }

            variableType = arguments[0];
            variableValueType = arguments[1];
            portValueType = arguments[2];
            return IsVisibleType(variableType) &&
                   IsVisibleType(variableValueType) &&
                   IsVisibleType(portValueType);
        }

        private static Type GetTargetType(FlowNode_ExecuteFunction node)
        {
            var arguments = GetGenericArguments(node);
            return arguments.Length > 0 ? arguments[0] : null;
        }

        private static Type GetPropertyTargetType(PropertyNode_PropertyValue node)
        {
            var arguments = GetGenericArguments(node);
            return arguments.Length > 0 ? arguments[0] : null;
        }

        internal static bool TryGetSelfReferenceTargetType(PropertyNode node, out Type targetType)
        {
            targetType = null;
            if (!IsGenericInstance(node, typeof(PropertyNode_GetSelfTReference<>)))
            {
                return false;
            }

            var portType = node.NodeData?.FindPortData("outputValue")?.GetValueType();
            if (portType != null &&
                typeof(UObject).IsAssignableFrom(portType) &&
                IsVisibleType(portType))
            {
                targetType = portType;
                return true;
            }

            var arguments = GetGenericArguments(node);
            if (arguments.Length == 0 ||
                !typeof(UObject).IsAssignableFrom(arguments[0]) ||
                !IsVisibleType(arguments[0]))
            {
                return false;
            }

            targetType = arguments[0];
            return true;
        }

        internal static Type[] GetGenericArguments(object value)
        {
            var type = value.GetType();
            while (type != null)
            {
                if (type.IsGenericType)
                {
                    return type.GetGenericArguments();
                }

                type = type.BaseType;
            }

            return Type.EmptyTypes;
        }

        internal static bool TryGetCustomExecutionEventBaseType(ExecutableEvent evt, out Type eventBaseType)
        {
            eventBaseType = null;
            var type = evt?.GetType();
            while (type != null)
            {
                if (type.IsGenericType &&
                    type.GetGenericTypeDefinition() == typeof(CustomExecutionEvent<>))
                {
                    eventBaseType = type.GetGenericArguments()[0];
                    return IsVisibleType(eventBaseType);
                }

                type = type.BaseType;
            }

            return false;
        }

        internal static bool IsGenericInstance(object value, Type genericTypeDefinition)
        {
            var type = value.GetType();
            while (type != null)
            {
                if (type.IsGenericType && type.GetGenericTypeDefinition() == genericTypeDefinition)
                {
                    return true;
                }

                type = type.BaseType;
            }

            return false;
        }

        internal static bool IsVisibleType(Type type)
        {
            if (type == null) return false;
            if (type.IsByRef || type.IsPointer || type.IsGenericParameter) return false;
            if (type.IsArray) return IsVisibleType(type.GetElementType());
            if (!IsVisibleTypeDefinition(type)) return false;

            return !type.IsGenericType || type.GetGenericArguments().All(IsVisibleType);
        }

        private static bool IsVisibleTypeDefinition(Type type)
        {
            if (!type.IsNested)
            {
                return type.IsPublic;
            }

            while (type != null && type.IsNested)
            {
                if (!type.IsNestedPublic)
                {
                    return false;
                }

                type = type.DeclaringType;
            }

            return type == null || type.IsPublic;
        }

