        internal static GeneratedSource GenerateSource(string displayName, FlowGraphData graphData,
            string className)
        {
            using var graph = graphData.CreateFlowGraphInstance();
            graph.AOT();
            var compiler = new FlowCSharpCompiler(new FlowCompilationContext(graph, displayName, className,
                FlowCSharpCompilationOptions.Current()));
            var generated = compiler.Compile();
            return new GeneratedSource(generated.Source, generated.FunctionDependencies);
        }

